def region_to_msk_offset(region: str):
    if not region:
        return None
    raw = region.strip().lower()
    if not raw or raw in ("-", "—"):
        return None
    # части-кандидаты: сначала хвост после последней '|' (там субъект), затем вся строка
    candidates = []
    if "|" in raw:
        candidates.append(raw.rsplit("|", 1)[-1].strip())
    candidates.append(raw)
    for s in candidates:
        for key, off in _KEYS:
            if key in s:
                return "МСК" if off == 0 else f"МСК{off:+d}"
    return None